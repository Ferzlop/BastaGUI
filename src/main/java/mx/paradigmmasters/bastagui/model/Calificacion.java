package mx.paradigmmasters.bastagui.model;

public class Calificacion {
    private int nombre, florFruto, pais, animal, color;

    public Calificacion() {
    }

    public int getNombre() {
        return nombre;
    }

    public int getFlorFruto() {
        return florFruto;
    }

    public int getPais() {
        return pais;
    }

    public int getAnimal() {
        return animal;
    }

    public int getColor() {
        return color;
    }
}
